#include <stdio.h>

int main() {
    int hex = 0x1A3;  // Hexadecimal
    int oct = 0723;   // Octal

    printf("Hexadecimal (0x1A3) in decimal: %d\n", hex);
    printf("Octal (0723) in decimal: %d\n", oct);
    printf("Decimal 419 in Hex: %X\n", hex);
    printf("Decimal 467 in Octal: %o\n", oct);

    return 0;
}
